CREATE TABLE contacts(
  id INT(11) AUTO_INCREMENT PRIMARY KEY,
  username VARCHAR(30),
  phone_number VARCHAR(11),
  method VARCHAR(10),
  message VARCHAR(300),
  call_message_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP);

